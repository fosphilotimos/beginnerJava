package com.company;

public class Main {

    public static void main(String[] args) {

        //print DooBeeDooBeeDo
        int x = 1;

        //Looping DooBee
        while (x < 3) {
            System.out.print("Doo");
            System.out.print("Bee");

            x = x + 1;
        }

        if (x == 3) {
            System.out.println("Do");
        }
    }
}
